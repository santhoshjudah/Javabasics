package sampleprogram;

public class Switchcase {

	public static void main(String[]args) {
		
		char vowel = 'a';
		
		switch(vowel){
		    case 'a' :
				System.out.println("it is an vowel");
			break ;
			
		    case 'e' :
				System.out.println("it is an vowel");
			break ;
			
		    case 'i' :
				System.out.println("it is an vowel");
			break ;
			
		    case 'o' :
				System.out.println("it is an vowel");
			break ;
			
		    case 'u' :
				System.out.println("it is an vowel");
			break ;
			
			default :
				System.out.println("its not a vowel");
			
		}
	}
}
