package sampleprogram;

public class Operators2 {
	
	public static void main(String[]args) {
		
		int a = 50;
		int b = 40;
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		System.out.println((a<b)&&(a==b));
		System.out.println((a<b)||(a==b));
		System.out.println(!((a<b)&&(a==b)));
		System.out.println(!((a<b)||(a==b)));
		

		
		
	
	}

}
